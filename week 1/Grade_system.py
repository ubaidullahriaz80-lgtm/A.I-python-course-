num = float(input('Enter the marks of math: '))
if num > 100 or num < 0:
    print('invalid')
elif num >= 90:
    print('Grade A+')
elif num >= 80:
    print('Grade A')
elif num >= 70:
    print('Grade B')
elif num >= 60:
    print('Grade C')
elif num >= 50:
    print('Grade D')
else:
    print('Fail')

